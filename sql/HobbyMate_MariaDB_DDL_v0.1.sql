SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS = 0;

CREATE TABLE IF NOT EXISTS HM_MEMBER (
    MEMBER_ID BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '회원 PK',
    LOGIN_ID VARCHAR(50) NOT NULL COMMENT '일반 로그인 아이디',
    PASSWORD VARCHAR(255) NOT NULL COMMENT '암호화 비밀번호',
    NAME VARCHAR(50) NOT NULL COMMENT '본인인증 이름',
    NICKNAME VARCHAR(50) NOT NULL COMMENT '서비스 닉네임',
    EMAIL VARCHAR(255) NOT NULL COMMENT '이메일',
    PHONE VARCHAR(30) NOT NULL COMMENT '휴대폰 번호',
    BIRTH_DATE DATE NOT NULL COMMENT '본인인증 생년월일',
    GENDER VARCHAR(10) NOT NULL COMMENT '성별: FEMALE 여성, MALE 남성',
    IDENTITY_VERIFIED_YN CHAR(1) NOT NULL DEFAULT 'N' COMMENT '본인인증 여부: Y 인증, N 미인증',
    IDENTITY_VERIFIED_AT DATETIME NULL COMMENT '본인인증 완료 일시',
    CI_HASH VARCHAR(255) NULL COMMENT '중복가입 확인용 CI 해시값',
    PROFILE_IMAGE_URL VARCHAR(1000) NULL COMMENT '프로필 이미지 UUID 저장 파일명',
    MEMBER_ROLE VARCHAR(20) NOT NULL DEFAULT 'USER' COMMENT '플랫폼 권한: USER 일반회원, ADMIN 관리자',
    MEMBER_STATUS VARCHAR(20) NOT NULL DEFAULT 'ACTIVE' COMMENT '회원 상태: ACTIVE 정상, SUSPENDED 정지, WITHDRAWN 탈퇴',
    WITHDRAWN_AT DATETIME NULL COMMENT '회원 탈퇴 일시',
    CREATED_AT DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '등록 일시',
    UPDATED_AT DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '수정 일시',
    PRIMARY KEY (MEMBER_ID),
    UNIQUE KEY UK_HM_MEMBER_LOGIN_ID (LOGIN_ID),
    UNIQUE KEY UK_HM_MEMBER_NICKNAME (NICKNAME),
    UNIQUE KEY UK_HM_MEMBER_EMAIL (EMAIL),
    UNIQUE KEY UK_HM_MEMBER_CI_HASH (CI_HASH),
    CONSTRAINT CK_HM_MEMBER_GENDER CHECK (GENDER IN ('FEMALE','MALE')),
    CONSTRAINT CK_HM_MEMBER_IDENTITY_VERIFIED_YN CHECK (IDENTITY_VERIFIED_YN IN ('Y','N')),
    CONSTRAINT CK_HM_MEMBER_ROLE CHECK (MEMBER_ROLE IN ('USER','ADMIN')),
    CONSTRAINT CK_HM_MEMBER_STATUS CHECK (MEMBER_STATUS IN ('ACTIVE','SUSPENDED','WITHDRAWN'))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='HobbyMate 회원';

CREATE TABLE IF NOT EXISTS HM_CATEGORY (
