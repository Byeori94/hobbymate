CREATE TABLE IF NOT EXISTS HM_CLUB (
    CLUB_ID BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '모임 PK',
    OWNER_MEMBER_ID BIGINT UNSIGNED NOT NULL COMMENT '모임장 회원 FK',
    CATEGORY_ID BIGINT UNSIGNED NOT NULL COMMENT '취미 카테고리 FK',
    CLUB_NAME VARCHAR(150) NOT NULL COMMENT '모임명',
    CLUB_SUMMARY VARCHAR(500) NOT NULL COMMENT '모임 한줄 소개',
    CLUB_DESCRIPTION LONGTEXT NOT NULL COMMENT '모임 상세 소개 HTML',
    REPRESENTATIVE_IMAGE_URL VARCHAR(1000) NULL COMMENT '모임 대표 이미지 URL',
    ACTIVITY_REGION VARCHAR(200) NULL COMMENT '주 활동 지역',
    GENDER_POLICY VARCHAR(10) NOT NULL DEFAULT 'MIXED' COMMENT '모집 성별: FEMALE 여성만, MALE 남성만, MIXED 혼성',
    MIN_AGE SMALLINT UNSIGNED NULL COMMENT '가입 최소 만 나이',
    MAX_AGE SMALLINT UNSIGNED NULL COMMENT '가입 최대 만 나이',
    MAX_MEMBER_COUNT INT UNSIGNED NOT NULL COMMENT '최대 모임 인원',
    JOIN_TYPE VARCHAR(20) NOT NULL DEFAULT 'APPROVAL' COMMENT '가입 방식: IMMEDIATE 즉시가입, APPROVAL 승인제',
    CLUB_STATUS VARCHAR(20) NOT NULL DEFAULT 'ACTIVE' COMMENT '모임 상태: ACTIVE 운영중, CLOSED 폐쇄, BLOCKED 관리자중지',
    RECRUIT_STATUS VARCHAR(20) NOT NULL DEFAULT 'OPEN' COMMENT '모집 상태: OPEN 모집중, FULL 정원마감, CLOSED 모집종료',
    CREATED_AT DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '등록 일시',
    UPDATED_AT DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '수정 일시',
    CLOSED_AT DATETIME NULL COMMENT '폐쇄 일시',
    PRIMARY KEY (CLUB_ID),
    KEY IX_HM_CLUB_OWNER_MEMBER_ID (OWNER_MEMBER_ID),
    KEY IX_HM_CLUB_CATEGORY_ID (CATEGORY_ID),
    KEY IX_HM_CLUB_STATUS (CLUB_STATUS, RECRUIT_STATUS),
    CONSTRAINT FK_HM_CLUB_OWNER_MEMBER FOREIGN KEY (OWNER_MEMBER_ID) REFERENCES HM_MEMBER (MEMBER_ID),
    CONSTRAINT FK_HM_CLUB_CATEGORY FOREIGN KEY (CATEGORY_ID) REFERENCES HM_CATEGORY (CATEGORY_ID),
    CONSTRAINT CK_HM_CLUB_GENDER_POLICY CHECK (GENDER_POLICY IN ('FEMALE','MALE','MIXED')),
    CONSTRAINT CK_HM_CLUB_JOIN_TYPE CHECK (JOIN_TYPE IN ('IMMEDIATE','APPROVAL')),
    CONSTRAINT CK_HM_CLUB_STATUS CHECK (CLUB_STATUS IN ('ACTIVE','CLOSED','BLOCKED')),
    CONSTRAINT CK_HM_CLUB_RECRUIT_STATUS CHECK (RECRUIT_STATUS IN ('OPEN','FULL','CLOSED')),
    CONSTRAINT CK_HM_CLUB_AGE_RANGE CHECK (MIN_AGE IS NULL OR MAX_AGE IS NULL OR MIN_AGE <= MAX_AGE),
    CONSTRAINT CK_HM_CLUB_MAX_MEMBER_COUNT CHECK (MAX_MEMBER_COUNT > 0)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='취미 모임';

CREATE TABLE IF NOT EXISTS HM_CLUB_MEMBER (
    CLUB_MEMBER_ID BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '모임 회원 PK',
    CLUB_ID BIGINT UNSIGNED NOT NULL COMMENT '모임 FK',
    MEMBER_ID BIGINT UNSIGNED NOT NULL COMMENT '회원 FK',
    CLUB_ROLE VARCHAR(20) NOT NULL DEFAULT 'MEMBER' COMMENT '모임 역할: LEADER 모임장, MANAGER 운영진, MEMBER 일반회원',
    MEMBER_STATUS VARCHAR(20) NOT NULL DEFAULT 'ACTIVE' COMMENT '모임 회원 상태: ACTIVE 활동중, WITHDRAWN 자진탈퇴, KICKED 강퇴',
    JOINED_AT DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '가입 일시',
    LEFT_AT DATETIME NULL COMMENT '탈퇴/강퇴 일시',
    PRIMARY KEY (CLUB_MEMBER_ID),
    UNIQUE KEY UK_HM_CLUB_MEMBER_CLUB_MEMBER (CLUB_ID, MEMBER_ID),
    KEY IX_HM_CLUB_MEMBER_MEMBER_ID (MEMBER_ID),
    KEY IX_HM_CLUB_MEMBER_STATUS (CLUB_ID, MEMBER_STATUS),
    CONSTRAINT FK_HM_CLUB_MEMBER_CLUB FOREIGN KEY (CLUB_ID) REFERENCES HM_CLUB (CLUB_ID),
    CONSTRAINT FK_HM_CLUB_MEMBER_MEMBER FOREIGN KEY (MEMBER_ID) REFERENCES HM_MEMBER (MEMBER_ID),
    CONSTRAINT CK_HM_CLUB_MEMBER_ROLE CHECK (CLUB_ROLE IN ('LEADER','MANAGER','MEMBER')),
    CONSTRAINT CK_HM_CLUB_MEMBER_STATUS CHECK (MEMBER_STATUS IN ('ACTIVE','WITHDRAWN','KICKED'))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='모임 가입 회원';

