CREATE TABLE IF NOT EXISTS HM_MEETING (
    MEETING_ID BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '만남 PK',
    CLUB_ID BIGINT UNSIGNED NOT NULL COMMENT '소속 모임 FK',
    CREATED_BY BIGINT UNSIGNED NOT NULL COMMENT '만남 등록자 회원 FK',
    MEETING_TITLE VARCHAR(200) NOT NULL COMMENT '만남명',
    MEETING_TYPE VARCHAR(20) NOT NULL COMMENT '만남 유형: OFFLINE 대면, ONLINE 비대면',
    START_DATETIME DATETIME NOT NULL COMMENT '시작 일시',
    END_DATETIME DATETIME NOT NULL COMMENT '종료 일시',
    APPLY_END_DATETIME DATETIME NOT NULL COMMENT '참여 신청 마감 일시',
    MAX_MEMBER_COUNT INT UNSIGNED NOT NULL COMMENT '최대 참여 인원',
    PARTICIPATION_FEE DECIMAL(12,2) NOT NULL DEFAULT 0 COMMENT '참가비',
    PREPARATION VARCHAR(1000) NULL COMMENT '준비물',
    MEETING_STATUS VARCHAR(20) NOT NULL DEFAULT 'RECRUITING' COMMENT '만남 상태: RECRUITING 모집중, CLOSED 모집마감, COMPLETED 완료, CANCELED 취소',
    PLACE_NAME VARCHAR(255) NULL COMMENT '대면 장소명',
    ROAD_ADDRESS VARCHAR(500) NULL COMMENT '대면 도로명 주소',
    JIBUN_ADDRESS VARCHAR(500) NULL COMMENT '대면 지번 주소',
    DETAIL_ADDRESS VARCHAR(500) NULL COMMENT '대면 상세 주소',
    LATITUDE DECIMAL(10,7) NULL COMMENT '대면 장소 위도',
    LONGITUDE DECIMAL(10,7) NULL COMMENT '대면 장소 경도',
    MAP_PROVIDER VARCHAR(20) NULL COMMENT '지도 제공사: KAKAO 카카오지도, NAVER 네이버지도',
    MAP_PLACE_ID VARCHAR(255) NULL COMMENT '지도 API 장소 식별자',
    VIDEO_PLATFORM VARCHAR(30) NULL COMMENT '화상회의 플랫폼: ZOOM, GOOGLE_MEET, MICROSOFT_TEAMS, DISCORD, OTHER',
    MEETING_URL VARCHAR(2000) NULL COMMENT '비대면 참여 링크',
    MEETING_PASSWORD VARCHAR(255) NULL COMMENT '비대면 회의 비밀번호',
    ONLINE_GUIDE VARCHAR(2000) NULL COMMENT '비대면 접속 안내',
    CREATED_AT DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '등록 일시',
    UPDATED_AT DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '수정 일시',
    CANCELED_AT DATETIME NULL COMMENT '취소 일시',
    PRIMARY KEY (MEETING_ID),
    KEY IX_HM_MEETING_CLUB_DATETIME (CLUB_ID, START_DATETIME),
    KEY IX_HM_MEETING_CREATED_BY (CREATED_BY),
    KEY IX_HM_MEETING_TYPE_STATUS (MEETING_TYPE, MEETING_STATUS),
    CONSTRAINT FK_HM_MEETING_CLUB FOREIGN KEY (CLUB_ID) REFERENCES HM_CLUB (CLUB_ID),
    CONSTRAINT FK_HM_MEETING_CREATED_BY FOREIGN KEY (CREATED_BY) REFERENCES HM_MEMBER (MEMBER_ID),
    CONSTRAINT CK_HM_MEETING_TYPE CHECK (MEETING_TYPE IN ('OFFLINE','ONLINE')),
    CONSTRAINT CK_HM_MEETING_STATUS CHECK (MEETING_STATUS IN ('RECRUITING','CLOSED','COMPLETED','CANCELED')),
    CONSTRAINT CK_HM_MEETING_MAP_PROVIDER CHECK (MAP_PROVIDER IS NULL OR MAP_PROVIDER IN ('KAKAO','NAVER')),
    CONSTRAINT CK_HM_MEETING_VIDEO_PLATFORM CHECK (VIDEO_PLATFORM IS NULL OR VIDEO_PLATFORM IN ('ZOOM','GOOGLE_MEET','MICROSOFT_TEAMS','DISCORD','OTHER')),
    CONSTRAINT CK_HM_MEETING_DATETIME CHECK (START_DATETIME < END_DATETIME),
    CONSTRAINT CK_HM_MEETING_APPLY_END CHECK (APPLY_END_DATETIME <= START_DATETIME),
    CONSTRAINT CK_HM_MEETING_MAX_MEMBER_COUNT CHECK (MAX_MEMBER_COUNT > 0),
    CONSTRAINT CK_HM_MEETING_FEE CHECK (PARTICIPATION_FEE >= 0)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='모임 내 대면/비대면 만남';

